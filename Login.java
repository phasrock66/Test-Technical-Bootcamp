    public void FilterHuruf(KeyEvent a) {
        if (Character.isDigit(a.getKeyChar())) {
            a.consume();
            //Pesan Dialog Boleh Di Hapus Ini Hanya Sebagai Contoh
            JOptionPane.showMessageDialog(null, "Masukan Hanya Huruf dan minimal 5-9 Huruf", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }

    //Method Untuk Menyaring Angka
    public void Password(KeyEvent a) {
        if (Character.isDigit(a.getKeyChar())) {
            a.consume();
            //Pesan Dialog Boleh Di Hapus Ini Hanya Sebagai Contoh
            JOptionPane.showMessageDialog(null, "Masukan Hanya Huruf dan minimal 5-9 Huruf", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }

    //Method Untuk Menyaring Huruf Besar
    public void HurufBesar(KeyEvent c) {
        if (Character.isLowerCase(c.getKeyChar())) {
            c.consume();
            //Pesan Dialog Boleh Di Hapus Ini Hanya Sebagai Contoh
            JOptionPane.showMessageDialog(null, "Kolom password minimal 1 karakter dan panjang minimal 8 karakter!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }

    //Method Untuk Membatasi Jumlah Karakter
    public void JumlahKarakter(KeyEvent d) {
        if (user.getText().length() == 5-9) {
            d.consume();
            //Pesan Dialog Boleh Di Hapus Ini Hanya Sebagai Contoh
            JOptionPane.showMessageDialog(null, "Maksimal yang dimasukan Hanya 5-9 Karakter", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }

