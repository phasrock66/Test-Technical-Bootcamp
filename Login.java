    private void prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesActionPerformed
        // TODO add your handling code here:
        if (user.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama user harus panjang 5-9 karakter!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (nama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "User harus kombinasi dari huruf kecil, huruf besar dan angka dengan panjang 5-9 karakter!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (pas.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom password minimal 1 karakter dan panjang minimal 8 karakter!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else if (konfir.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom Password Tidak Sama!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan!!");
        }
    }//GEN-LAST:event_prosesActionPerformed

